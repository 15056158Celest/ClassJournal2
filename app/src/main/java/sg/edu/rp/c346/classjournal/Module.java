public class Module {
    private String code;
    private String name;
    private int week;




    public Module(String code, String name, int week){
        this.code =code;
        this.name=name;
        this.week=week;



    }


    public String getCode() {
        return code;
    }
    public String getName() {
        return name;
    }

    public Integer getWeek() {
        return week;
    }








}


